	# List Comprehension --> cubos

cubos = [value ** 3 for value in range(1, 11)]
print(cubos)

	# Usando métodos de fatia da lista

print('Os três primeiros itens da lista são ' + str(cubos[0:3]))
print('Os três primeiros intens do meio da lista são ' + str(cubos[4:7]))
print('Os três primeiros itens da lista são ' + str(cubos[7:10]))
