# USANDO O FOR JUNTO COM O IF.

animais = ['rato', 'cachorro', 'gato', 'tartaruga']
for animal in animais:
	if animal == 'tartaruga':
		print('Uma ' + animal.title() + ' seria um ótimo animal de estimação.')
	else:
		print('Um ' + animal.title() + ' seria um ótimo animal de estimação.')
print('\nQualquer um desses animais seria um ótimo animal de estimação!')
